// Build phase groups for story sidebar
export function getPhaseGroups() {
  const groups = [];
  let lastGroup = null;
  EXPERIMENTS.forEach(exp => {
    if (exp.phaseGroup !== lastGroup) {
      groups.push({ label: exp.phaseGroup, experiments: [] });
      lastGroup = exp.phaseGroup;
    }
    groups[groups.length - 1].experiments.push(exp);
  });
  return groups;
}
